// Java Program to implement Relational Operators
import java.io.*;

// Driver Class
class GFG {
	// main function
	public static void main(String[] args)
	{
		// Comparison operators
		int a = 10;
		int b = 3;
		int c = 5;

		System.out.println("a > b: " + (a > b));
		System.out.println("a < b: " + (a < b));
		System.out.println("a >= b: " + (a >= b));
		System.out.println("a <= b: " + (a <= b));
		System.out.println("a == c: " + (a == c));
		System.out.println("a != c: " + (a != c));
	}
}

