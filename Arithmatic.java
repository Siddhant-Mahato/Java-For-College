
public class Arithmatic {
    
    public static void main(String[] args) {
        int p = 20, q = 10;
        System.out.println("Result is = "+(p+q));
        System.out.println("Result is = "+(p-q));
        System.out.println("Result is = "+(p*q));
        System.out.println("Result is = "+(p/q));
        System.out.println("Result is = "+(p%q));

        System.out.println("Result is - " + (p==q));
        System.out.println("Result is - " + (p!=q));
        System.out.println("Result is - " + (p>=q));
        System.out.println("Result is - " + (p<=q));
    }
    
}
